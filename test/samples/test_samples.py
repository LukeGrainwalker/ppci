def enable_report_logger(filename):
    logging.getLogger().setLevel(logging.DEBUG)
    fh = logging.StreamHandler(filename)
    logging.getLogger().addHandler(fh)


def only_bf(txt):
    """Strip a string from all characters, except brainfuck chars"""
    return re.sub(r"[^\.,<>\+-\]\[]", "", txt)


